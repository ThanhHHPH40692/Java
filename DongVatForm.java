    private void fillData(DongVat dv) {
        txtID.setText(dv.getId());
        txtName.setText(dv.getName());
        cbbLoai.setSelectedItem(dv.getLoai());
        if (dv.getGender().contains("Đực")) {
            rdDuc.setSelected(true);
        } else {
            rdCai.setSelected(true);
        }
    }

