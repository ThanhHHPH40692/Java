    private void loadTable() {
        model = (DefaultTableModel) txtTable.getModel();
        model.setColumnCount(0);
        model.setRowCount(0);
        model.addColumn("ID");
        model.addColumn("Name");
        model.addColumn("Giới tính");
        model.addColumn("Loài");
        model.addColumn("Cân nặng");
        for (DongVat dv : list) {
            Object[] row = new Object[]{
                dv.getId(),
                dv.getName(),
                dv.getGioiTinh(),
                dv.getLoai(),
                dv.getCanNang()
            };
            model.addRow(row);
        }
    }

