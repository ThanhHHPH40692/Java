    private void loadTable() {
        model = (DefaultTableModel) txtTable.getModel();
        model.setColumnCount(0);
        model.addColumn("ID");
        model.addColumn("Name");
        model.addColumn("Loài");
        model.addColumn("Giới Tính");
        model.setRowCount(0);
        for (DongVat dv : list) {
            Object[] row = new Object[]{
                dv.getId(),
                dv.getName(),
                dv.getLoai(),
                dv.getGender()
            };
            model.addRow(row);
        }
    }

